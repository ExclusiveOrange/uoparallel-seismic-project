#include <stddef.h>


////////////////////////////////////////////////////////////////////////////////
// typedefs
////////////////////////////////////////////////////////////////////////////////

typedef int coord_t;


////////////////////////////////////////////////////////////////////////////////
// structs
////////////////////////////////////////////////////////////////////////////////

struct POINT3D {
    coord_t x, y, z;
};


////////////////////////////////////////////////////////////////////////////////
// functions
////////////////////////////////////////////////////////////////////////////////

void
point3dset (
    struct POINT3D *pt,
    int x, int y, int z
)
{
    pt->x = x;
    pt->y = y;
    pt->z = z;
}


